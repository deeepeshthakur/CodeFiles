long long pow_mod(long long a, long long b, long long mod){
    long long ans=1;
    a%=mod;
    while(b>0){
        if(b&1){
            ans*=a;
            ans%=mod;
        }
        b=b>>1;
        a=(a*a)%mod;
    }
    return ans;
}

void solve(){
    int n;
    cin>>n;
    for(int i=1;i<=n;i++){
        if(i==n){
            cout<<"10\n";
            continue;
        }
        cout<<(1ll*(9*n-9*i+11)*9ll*pow_mod(10ll,1ll*n-i-1,MOD))%MOD<<" ";
    }
}

