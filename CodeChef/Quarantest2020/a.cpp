void solve(){
    int n;cin>>n;
    vector<vector<int>> a(n,vector<int>(n,0));
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            cin>>a[i][j];
        }
    }
    for(int i=0;i<n;i++){
        for(int j=0;j<n;j++){
            if(a[i][j]==1 && ((i+1<n && a[i+1][j]==1)||(j+1<n && a[i][j+1]))){
                cout<<"UNSAFE\n";
                return;
            }
        }
    }
    cout<<"SAFE\n";
}

